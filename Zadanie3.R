# Raport obejmuje informacje dotyczące pakietu ioanalisis

Pakiety które się w nim znajdują to ggplot2 , plot3D, lpSolve. \# w celu wczytaniago użyłem komendy

```{r}
> install.packages("ioanalysis")
```

i w celu zapoznania się z poszczegulnymi pakietami użyłem komendy

```{r}
> library(ioanalysis)
```

i do zapoznania się z poszczególnymi jego elementami i komedami użyłem komendy
```{r}
> ?ioanalysis
```
Pakiet wydaje się służyć jako narzędzie do tworzenia list input , output w celu oszczędzania czasu w pracy nad takimi danymi. Dotyczny on i odnosi się di kalkulacji Goshian inverse i Leontief inverse . Wydają się to być terminy matematyczne
## Przykład kodu 
```{r}
>as.inputoutput(E)

```
*jest kodem odpowiedzialnymza podstawe pakiety w którewstawiane są przykładowo dane E, czyli eksporty*
# Konkluzje
Jest to pakiet przeznaczony do analiz których nie rozumiem w **tym momencie**
